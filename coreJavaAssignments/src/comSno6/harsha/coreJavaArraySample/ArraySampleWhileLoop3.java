package comSno6.harsha.coreJavaArraySample;

public class ArraySampleWhileLoop3
{
    public static void main(final String[] args) {
        int[] j;
        j = new int []{ 5, 8, 5, 9, 1 };
        
        int i = 0;
        while(i < j.length) {
        	System.out.println(j[i]);
        	i++;
        }
    }
}

